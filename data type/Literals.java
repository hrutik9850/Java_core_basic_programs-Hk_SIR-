class Literals

{
	public static void main(String[] args) 
	{
		System.out.println(123);
		System.out.println(01012);
		System.out.println(0b1010);
		System.out.println(0xabc);
		System.out.println(13456);
		System.out.println(0237);
		System.out.println(0x27d);
		System.out.println();
		System.out.println(0b101);
		System.out.println(0b101);
		System.out.println();
		float b1= 0b101;
		System.out.println(b1);
		float b2 = 0xabc;
		System.out.println(b2);
		short b3=231;
		System.out.println(b3);
		char b4=0b101011;				//print to the (+) character 
		System.out.println(b4);

	}
}
